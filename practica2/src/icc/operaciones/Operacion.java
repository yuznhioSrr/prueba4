
package icc.operaciones;

/**
 * 
 * @author
 */
public class Operacion {

    /**
    * Simula la suma en numeros enteros.
    * @param a Primer operando de la suma.
    * @param b Segundo operando de la suma.
    * @return Resultado de la suma.
    */
    public int suma(int a, int b){
        return a + b;
    }

    /**
    * Simula la multiplicacion en numeros enteros.
    * @param a Primer operando de la multiplicacion.
    * @param b Segundo operando de la multiplicacion.
    * @return Resultado de la multiplicacion.
    */
    public int multiplicacion(int a, int b){
        return a * b;
    }

    /**
    * Simula la resta en numeros enteros.
    * @param a Primer operando de la resta.
    * @param b Segundo operando de la resta.
    * @return Resultado de la resta.
    */
    public int resta(int a, int b){
        return a - b;
    }

    /**
    * Simula la division en numeros enteros.
    * @param a Primer operando de la division.
    * @param b Segundo operando de la division.
    * @return Resultado de la division.
    */
    public int division(int a, int b){
        return a / b;
    }

    /**
    * Simula el modulo en numeros enteros.
    * @param a Primer operando de el modulo.
    * @param b Segundo operando de el modulo.
    * @return Resultado de el modulo.
    */
    public int modulo(int a, int b){
        return a % b;
    }

}
